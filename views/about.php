            <p class="sec-desc mt-n2 mb-30">Tudo começou quando Sabrina ainda era criança, aos 11 anos, ao ingressar no Projeto Prodança em Campinas, São Paulo, Brasil. Desde então, Sabrina Rodrigues acumulou mais de 25 anos de experiência, dedicando sua vida à arte da dança.
Desde muito jovem, Sabrina demonstrou uma paixão e um talento excepcionais pela dança. Sua determinação e dedicação a levaram a superar inúmeros desafios ao longo de sua trajetória. Com o passar dos anos, suas habilidades e conhecimentos se expandiram, permitindo-lhe mergulhar em diversas culturas e estilos de dança ao redor do mundo.
Sua carreira internacional a levou a mais de 25 países, incluindo destinos como Cuba, México, França, Portugal, Argentina, Uruguai, Japão, Alemanha, Itália, Turquia, Marrocos, Croácia, Grécia, Inglaterra, Espanha e muitos outros. Cada país visitado trouxe novas influências e ensinamentos, enriquecendo seu repertório e moldando-a como uma dançarina versátil e completa.
No Brasil, Sabrina teve uma carreira igualmente impressionante. Ela atuou em programas de TV, incluindo a Rede TV, onde foi assistente e bailarina do apresentador Gilberto Barros, e no SBT, no programa de Silvio Santos. Sua presença na mídia ajudou a popularizar sua arte e a inspirar muitos jovens a seguirem seus passos.
Além disso, Sabrina sempre se mostrou comprometida com a educação na dança, lecionando em academias, escolas e projetos sociais. Seu compromisso com o ensino e a formação de novos talentos a consolidou como uma referência no cenário da dança, tanto no Brasil quanto internacionalmente.
Em 2024, um dos maiores sonhos de Sabrina se concretizou com a abertura do Studio de Dança Sabrina Rodrigues. Este espaço próprio não só representou um marco significativo em sua carreira, mas também um local onde ela pode compartilhar sua paixão e conhecimento com seus alunos de forma mais direta e impactante. No estúdio, Sabrina se dedica a ensinar não apenas técnicas de dança, mas também valores importantes como disciplina, respeito, socialização, perseverança e superação de desafios.
</p>
            

